# vypocet vzdalenosti mezi mesty (kazde s kazdym) pro vyse zadany
# dataset, pocitano ze souradnic (lat, lon) na zemekouli, vraci
# matici vzdalenosti, samotne souradnice a pocet mest.
def calc_city_distances(coordDict):

    cities = list(coordDict.keys())
    n = len(cities)
    distances = {}
    matrix = []
    points = []
    for i in range(n):
        points.append(coordDict[cities[i]])
        cityA = cities[i]
        latA, longA, coordXA, coordYA = coordDict[cityA]
        latA = radians(latA)
        longA = radians(longA)
        row = []
        for j in range(n):
            cityB = cities[j]
            key = frozenset((cityA, cityB))
            latB, longB, coordXB, coordYB = coordDict[cityB]
            latB = radians(latB)
            longB = radians(longB)
            dLong = abs(longA - longB)
            if i != j:
                angle = acos(sin(latA)*sin(latB)+cos(latA)*cos(latB)*cos(dLong))
                dist = angle * 6371.1 # Mean Earth radius (km)
            else:
                dist = 0
            distances[key] = dist
            row.append(dist)
        matrix.append(row)

    return(matrix, points, n, distances)